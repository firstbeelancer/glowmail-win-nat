export type TranslationKey = keyof typeof translations.en;

export function t(key: TranslationKey, language: Language, params?: Record<string, string>): string {
  let text: string = translations[language]?.[key] || translations.en[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}

const folderNameMap: Record<string, TranslationKey> = {
  inbox: 'folder.inbox',
  sent: 'folder.sent',
  outbox: 'folder.outbox',
  drafts: 'folder.drafts',
  spam: 'folder.spam',
  trash: 'folder.trash',
  work: 'folder.work',
};

export function translateFolderName(folderId: string, folderName: string, language: Language): string {
  const key = folderNameMap[folderId];
  if (key) return t(key, language);
  return folderName;
}
